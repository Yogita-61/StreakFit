const formatMinutes = (minutes) => {
  if (minutes < 1) return '1 min'
  const rounded = Math.ceil(minutes)
  return `${rounded} min`
}

export const getWorkoutDuration = (workout) => {
  const sets = Number(workout.sets) || 1
  const reps = workout.reps

  if (typeof reps === 'string') {
    const value = Number.parseFloat(reps)
    if (reps.includes('sec')) {
      const activeSeconds = sets * value
      const restSeconds = Math.max(sets - 1, 0) * 30
      return formatMinutes((activeSeconds + restSeconds) / 60)
    }

    if (reps.includes('min')) {
      const activeMinutes = sets * value
      const restMinutes = Math.max(sets - 1, 0) * 0.5
      return formatMinutes(activeMinutes + restMinutes)
    }
  }

  const activeSeconds = sets * Number(reps) * 3
  const restSeconds = Math.max(sets - 1, 0) * 45
  return formatMinutes((activeSeconds + restSeconds) / 60)
}

export const getWorkoutsByCategory = (category) => {
  const workouts = workoutData[category] || workoutData.Normal
  return workouts.map(workout => ({
    ...workout,
    duration: workout.duration || getWorkoutDuration(workout)
  }))
}
